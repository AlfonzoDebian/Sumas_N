# Solicitar al usuario que ingrese un número entero positivo
n = input("Digite el número: ")
n = int(n)

# Calcular la suma de los primeros n números naturales usando la fórmula s = n(n+1)/2
s = (n * (n + 1)) / 2

# Imprimir el resultado con un mensaje decorado
print("="*30)
print("     CALCULO DE SUMA     ")
print("="*30)
print(f"La suma de los primeros {n} números naturales es: {s}")
print("="*30)


